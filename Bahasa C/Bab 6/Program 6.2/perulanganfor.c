#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[])
{
  int i;
  for(i=1;i<=5;i++)
  {
    printf("%i\n",i);
  }
  printf("\n");
  for(i=5;i>=1;i--)
  {
    printf("%i\n",i);
  }
  printf("\n");
    
  return 0;
}
