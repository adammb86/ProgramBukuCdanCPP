#include <cstdlib>
#include <iostream>

using namespace std;

int main(int argc, char *argv[])
{
    int i;
    for(i=1;i<=5;i++)
    {
         cout<<i<<endl;
    }
    cout<<endl;
    for(i=5;i>=1;i--)
    {
         cout<<i<<endl;
    }
    cout<<endl;

    return EXIT_SUCCESS;
}
